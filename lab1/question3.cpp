#include <iostream>
using namespace std;

int main()
{
    int a, b;

    cout << "Enter first number: ";
    cin >> a;

    cout << "Enter second number: ";
    cin >> b;

    cout << "\nAddition: " << a + b;
    cout << "\nSubtraction: " << a - b;
    cout << "\nMultiplication: " << a * b;
    cout << "\nDivision: " << a / b;

    return 0;
}
