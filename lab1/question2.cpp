#include <iostream>
using namespace std;

int main()
{
    int a;
    float b;
    char c;

    cout << "Enter an integer: ";
    cin >> a;

    cout << "Enter a decimal number: ";
    cin >> b;

    cout << "Enter a character: ";
    cin >> c;

    cout << "\nInteger: " << a;
    cout << "\nDecimal: " << b;
    cout << "\nCharacter: " << c;

    return 0;
}
